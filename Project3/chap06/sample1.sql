INSERT INTO EMP_TEMP (
    SELECT EMPLOYEE_ID
          ,FIRST_NAME ||'' || LAST_NAME
          ,SALARY
          ,DEPARTMENT_ID
FROM EMPLOYEES
);

--다른 테이블과 조인해서 조회해서 새 테이블 생성

CREATE TABLE EMP_TEMP2 (
     EMP_ID NUMBER(6)     NOT NULL
    ,NAME  VARCHAR2(20)  NOT NULL
    ,SALARY NUMBER(8,2)
    ,DEPT_NAME VARCHAR(30) NOT NULL
);

INSERT INTO EMP_TEMP2 (
    SELECT A.EMPLOYEE_ID
          ,A.FIRST_NAME ||'' || LAST_NAME
          ,A.SALARY
          ,B.DEPARTMENT_ID
FROM EMPLOYEES A JOIN DEPARTMENTS B
ON A.DEPARTMENT_ID = B.DEPARTMENT_ID
);

SELECT * FROM EMP_TEMP2;

-- 조회 결과에 맞추어 테이블 생성 및 데이터 추가, INSERT 과정을 굳이 안 써도 된다.
-- 테이블 명 뒤에 꼭 AS를 붙여줘야 한다.
CREATE TABLE EMP_TEMP3 AS
     SELECT A.EMPLOYEE_ID AS EMP_ID
          ,A.FIRST_NAME ||'' || LAST_NAME AS NAME
          ,A.SALARY AS SALARY
          ,B.DEPARTMENT_ID AS DEPT_NAME
FROM EMPLOYEES A JOIN DEPARTMENTS B
ON A.DEPARTMENT_ID = B.DEPARTMENT_ID;

SELECT * FROM EMP_TEMP3;

