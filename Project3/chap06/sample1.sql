-- 다른 테이블이 가진 데이터 기반으로 데이터를 추가 한다면 일일이 INSERT 작업을 하지 않고서도 
-- 조회 후 추가 하는 형태로 할 수 있다. 
CREATE TABLE EMP_TEMP (
    EMP_ID NUMBER(6)     NOT NULL
    ,NAME  VARCHAR2(20)  NOT NULL
    ,SALARY NUMBER(8,2)
    ,DEPT_ID NUMBER(4)
);
