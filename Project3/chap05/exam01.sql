-- 3. 모든 부서의 위치(주소) 조회
--   (COUNTRIES, REGIONS, LOCATIONS, DEPARTMENTS)
SELECT * FROM DEPARTMENTS;
SELECT * FROM REGIONS;
SELECT * FROM COUNTRIES;
SELECT * FROM LOCATIONS;

SELECT DEPARTMENT_ID AS 부서코드
       ,DEPARTMENT_NAME AS 부서명
       ,LOCATION_ID AS 주소코드
       ,(SELECT STREET_ADDRESS FROM LOCATIONS B WHERE B.LOCATION_ID = A.LOCATION_ID) AS 주소
 FROM DEPARTMENTS A ;

 SELECT A.DEPARTMENT_NAME AS 부서명
     , D.REGION_NAME || ' ' ||
       C.COUNTRY_NAME  || ' ' ||
       B.STATE_PROVINCE  || ' ' ||
       B.CITY  || ' ' ||
       B.STREET_ADDRESS  || ' ' ||
       B.POSTAL_CODE AS 영문주소
  FROM DEPARTMENTS A JOIN LOCATIONS B
    ON A.LOCATION_ID = B.LOCATION_ID
  JOIN COUNTRIES C
    ON B.COUNTRY_ID = C.COUNTRY_ID
  JOIN REGIONS D
    ON C.REGION_ID = D.REGION_ID;

-- 4. 아프리카 지역에 해당하는 부서와 소속 사원들을 조회
--  (COUNTRIES, REGIONS, LOCATIONS, DEPARTMENTS, EMPLOYEES )

SELECT B.DEPARTMENT_NAME AS 부서명
     , A.FIRST_NAME || ' ' || A.LAST_NAME AS 이름
  FROM EMPLOYEES A JOIN DEPARTMENTS B
    ON A.DEPARTMENT_ID = B.DEPARTMENT_ID
  JOIN LOCATIONS C
    ON B.LOCATION_ID = C.LOCATION_ID
  JOIN COUNTRIES D
    ON C.COUNTRY_ID = D.COUNTRY_ID
  JOIN REGIONS E
    ON D.REGION_ID = E.REGION_ID
 WHERE D.REGION_ID = 1
 ORDER BY 1;

-- 5. 소속 지역별 평균급여 랭크 조회(지역은 대륙단위로 묶어서 한다.)
--  (COUNTRIES, REGIONS, LOCATIONS, DEPARTMENTS, EMPLOYEES)
SELECT RANK() OVER(ORDER BY AVG(A.SALARY) DESC) AS 랭킹
     , TRUNC(AVG(A.SALARY)) AS 평균급여
     , E.REGION_NAME AS 국가명
  FROM EMPLOYEES A JOIN DEPARTMENTS B
    ON A.DEPARTMENT_ID = B.DEPARTMENT_ID
  JOIN LOCATIONS C
    ON B.LOCATION_ID = C.LOCATION_ID
  JOIN COUNTRIES D
    ON C.COUNTRY_ID = D.COUNTRY_ID
  JOIN REGIONS E
    ON D.REGION_ID = E.REGION_ID
 GROUP BY E.REGION_NAME;

-- 6. 국가별 평균급여 랭크 조회
--    (COUNTRIES, LOCATIONS, DEPARTMENTS, EMPLOYEES)
SELECT RANK() OVER(ORDER BY AVG(A.SALARY) DESC) AS 랭킹
     , TRUNC(AVG(A.SALARY)) AS 평균급여
     , D.COUNTRY_NAME AS 국가명
  FROM EMPLOYEES A JOIN DEPARTMENTS B
    ON A.DEPARTMENT_ID = B.DEPARTMENT_ID
  JOIN LOCATIONS C
    ON B.LOCATION_ID = C.LOCATION_ID
  JOIN COUNTRIES D
    ON C.COUNTRY_ID = D.COUNTRY_ID
 GROUP BY D.COUNTRY_NAME;

-- 7. 국가별/부서별 평균급여 조회(전체 평균 및 국가 단위 평균, 부서 단위 평균의 통계가 필요)
--    (COUNTRIES, LOCATIONS, DEPARTMENTS, EMPLOYEES)
SELECT TRUNC(AVG(A.SALARY)) AS 평균급여
     , D.COUNTRY_NAME AS 국가명
     , B.DEPARTMENT_NAME AS 부서명
  FROM EMPLOYEES A JOIN DEPARTMENTS B
    ON A.DEPARTMENT_ID = B.DEPARTMENT_ID
  JOIN LOCATIONS C
    ON B.LOCATION_ID = C.LOCATION_ID
  JOIN COUNTRIES D
    ON C.COUNTRY_ID = D.COUNTRY_ID
 GROUP BY CUBE(D.COUNTRY_NAME, B.DEPARTMENT_NAME);