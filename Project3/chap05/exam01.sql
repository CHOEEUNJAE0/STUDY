-- 1. 매니저가 존재하는 부서의 사원들을 조회
--  (DEPARTMENTS, EMPLOYEES)
--서브쿼리 이용
SELECT EMPLOYEE_ID 
      ,FIRST_NAME
      ,LAST_NAME
      ,DEPARTMENT_ID
      ,(SELECT DEPARTMENT_NAME FROM DEPARTMENTS B WHERE B.DEPARTMENT_ID = A.DEPARTMENT_ID) AS DEPARTMENT_NAME
FROM EMPLOYEES A
WHERE DEPARTMENT_ID IN (SELECT DEPARTMENT_ID
                        FROM DEPARTMENTS 
                        WHERE MANAGER_ID IS NOT NULL);

-- JOIN 이용
SELECT A.EMPLOYEE_ID AS 사원코드
      ,A.DEPARTMENT_ID AS 부서코드
      ,CONCAT(FIRST_NAME, '' || LAST_NAME) AS 사원명
      ,B.DEPARTMENT_NAME AS 부서명
      ,B.MANAGER_ID AS 관리자코드
FROM EMPLOYEES A JOIN DEPARTMENTS B
ON (A.MANAGER_ID = B.MANAGER_ID)
WHERE B.MANAGER_ID IS NOT NULL--  NULL값은 뺴주세요~
ORDER BY 1;    

