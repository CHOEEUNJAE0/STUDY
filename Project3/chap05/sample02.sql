-- 1. 매니저가 존재하는 부서의 사원들을 조회
--  (DEPARTMENTS, EMPLOYEES)
SELECT A.DEPARTMENT_ID AS 부서코드
      ,CONCAT(FIRST_NAME, '' || LAST_NAME) AS 사원명
      ,B.DEPARTMENT_NAME AS 부서명
      ,B.MANAGER_ID AS 관리자코드
FROM EMPLOYEES A JOIN DEPARTMENTS B
ON (A.MANAGER_ID = B.MANAGER_ID);
    
-- 2. 유럽, 아메리카 , 아시아, 중앙아시아/아프리카 지역 (REGIONS)에 있는 부서의 수를 조회
--  (REGIONS, COUNTRIES)
SELECT * FROM REGIONS;
SELECT * FROM COUNTRIES;

SELECT A.COUNTRY_NAME AS 국가명
      ,COUNT(B.REGION_ID) AS 지역코드
FROM COUNTRIES A JOIN REGIONS B
ON A.REGION_ID = B.REGION_ID
GROUP BY COUNTRY_ID, COUNTRY_NAME;

-- 3. 모든 부서의 위치(주소) 조회
