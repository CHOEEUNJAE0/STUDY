-- SELECT 절에 SUBQUERY 사용 -> SCALAR(스칼라) SUBQUERY
-- SCALAR : 단일 값 OR 정해진 값 이라고 보면 된다.
SELECT EMPLOYEE_ID,
       FIRST_NAME,
       LAST_NAME,
       EMAIL,
       PHONE_NUMBER,
       HIRE_DATE,
       JOB_ID, 
       (SELECT JOB_TITLE FROM JOBS B WHERE B.JOB_ID = A.JOB_ID) AS JOB_TITLE,--하나의 컬럼을 꼭 지정해서 넣어줘야 한다.
       SALARY,
       COMMISSION_PCT,
       MANAGER_ID, 
       DEPARTMENT_ID,
       (SELECT DEPARTMENT_NAME FROM DEPARTMENTS C WHERE C.DEPARTMENT_ID = A.DEPARTMENT_ID) AS DEPARTMENT_NAME
 FROM EMPLOYEES A ;

-- WHERE 절에 SUBQUERY 사용 -> 상호연관 SUBQUERY 

SELECT *
FROM EMPLOYEES
WHERE SALARY >= (SELECT AVG(SALARY) FROM EMPLOYEES );

SELECT *
FROM EMPLOYEES
WHERE EMPLOYEE_ID IN (SELECT DISTINCT MANAGER_ID FROM EMPLOYEES WHERE MANAGER_ID IS NOT NULL);

-- FROM 절에 SUBQUERY 사용 -> INLINE VIEW(인라인뷰) 라고 함.
SELECT * 
FROM  (SELECT EMPLOYEE_ID, FIRST_NAME, LAST_NAME FROM EMPLOYEES);
-- 괄호 안에 것들로 테이블 생성 후 그것들을 SELECT 하겠다

-- WITH를 사용하면 임시로 이름을 지정한 테이블 생성 가능
WITH TEMP AS (
    SELECT EMPLOYEE_ID,
           FIRST_NAME,
           LAST_NAME
    FROM EMPLOYEES
)
SELECT * FROM TEMP;

-- 랭킹 조회
SELECT * 
FROM (SELECT EMPLOYEE_ID,
             FIRST_NAME,
             LAST_NAME,
             SALARY,
             RANK() OVER (ORDER BY SALARY DESC) AS "RANK"
FROM EMPLOYEES
ORDER BY SALARY DESC);

-- WHERE로 조건 주기
SELECT * 
FROM (SELECT EMPLOYEE_ID,
             FIRST_NAME,
             LAST_NAME,
             SALARY,
             RANK() OVER (ORDER BY SALARY DESC) AS "RANK"
FROM EMPLOYEES)
WHERE "RANK" <= 10;

--DENSE RANK : 똑같은 값이 나와도 랭킹값이 올라가지 않고 같은 랭킹값이 나오는 것.
SELECT * 
FROM (SELECT EMPLOYEE_ID,
             FIRST_NAME,
             LAST_NAME,
             SALARY,
             DENSE_RANK() OVER (ORDER BY SALARY DESC) AS "RANK"
FROM EMPLOYEES)
WHERE "RANK" <= 10;



