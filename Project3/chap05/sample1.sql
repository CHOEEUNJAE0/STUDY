-- JOIN을 수행하는 테이블의 컬럼이 동일한 이름을 사용하는 경우 테이블명.컬럼명 으로 구분시킨다.
SELECT EMPLOYEES.EMPLOYEE_ID AS 사원코드,
       CONCAT(EMPLOYEES.FIRST_NAME, '' || EMPLOYEES.LAST_NAME) AS 사원명,
       EMPLOYEES.DEPARTMENT_ID AS 부서코드,
       DEPARTMENTS.DEPARTMENT_NAME AS 부서명
FROM EMPLOYEES JOIN DEPARTMENTS
ON (EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID);

 
-- JOIN 쿼리문 작성 시 테이블 명을 명시 해주면 좀 더 정확하게 표시 가능.
-- 테이블 명 다 작성해주기 좀 번거로우니깐 A B 등으로 간단하게 별칭 부여 해 줄 수 있다.
SELECT A.EMPLOYEE_ID AS 사원코드,
       CONCAT(A.FIRST_NAME, '' || A.LAST_NAME) AS 사원명,
       A.DEPARTMENT_ID AS 부서코드,
       B.DEPARTMENT_NAME AS 부서명
FROM EMPLOYEES A JOIN DEPARTMENTS B
ON (A.DEPARTMENT_ID = B.DEPARTMENT_ID);

-- JOIN 조건의 컬럼명이 동일 한 경우에만 USING을 사용하여 결합 시킬 수 있다.
-- 대신 해당 컬럼명 앞에는 테이블명. 을 붙여주면 안된다.
-- USING(컬럼명) 한번만 써 주면 된다.
SELECT A.EMPLOYEE_ID AS 사원코드,
       CONCAT(A.FIRST_NAME, '' || A.LAST_NAME) AS 사원명,
       DEPARTMENT_ID AS 부서코드,
       B.DEPARTMENT_NAME AS 부서명
FROM EMPLOYEES A JOIN DEPARTMENTS B
USING (DEPARTMENT_ID);

