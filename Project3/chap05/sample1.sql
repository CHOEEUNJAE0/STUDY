-- JOIN : 2개 이상의 테이블에서 데이터를 조회하기 위해 사용하는 구문
--        집합 구문과는 다르게 두 쿼리 결과에 대한 무조건적인 집합셋(쿼리셋)을 구하는 것이 아닌
--        쿼리 결과에 대한 결합 조건이 있다. JOIN 시켜 주는 것.
--        집합 구문에서는 행(로우) 데이터에 대한 추가가 이루어지지만,
--        JOIN은 열(컬럼) 데이터에 대한 추가가 이루어 진다.

SELECT * 
FROM EMPLOYEES JOIN DEPARTMENTS
ON (EMPLOYEES.DEPARTMENT_ID = DEPARTMENTS.DEPARTMENT_ID);

